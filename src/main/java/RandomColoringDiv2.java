    public int getCount(int maxR, int maxG, int maxB, int startR, int startG, int startB, int d1, int d2) {
        int res = 0;
        
        for (int i=0; i<maxR;i++)
        	for (int j=0; j<maxG;j++)
        		for (int k=0; k<maxB;k++)
        			if (((Math.abs(i-startR) <= d2) && (Math.abs(j-startG) <= d2) && (Math.abs(k-startB) <= d2))
        					&& ((Math.abs(i-startR) >= d1) || (Math.abs(j-startG) >= d1) || (Math.abs(k-startB) >= d1))){
        				res ++;
        			}
        
        return res;
    }

